package com.kailynwilliams;

public class CalculatorFunctions {
    public int total;

   public int multiply(int input1, int input2){
        total= input1*input2;
       return total;
    }

    public int addition(int input1, int input2){
        total= input1+input2;
       return total;
    }

    public int subtract(int input1, int input2){
        total= input1-input2;
       return total;
    }

    public int divide(int input1, int input2){
        total= input1/input2;
       return total;
    }




}
